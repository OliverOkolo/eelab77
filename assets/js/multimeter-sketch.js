/**
 * multimeter-sketch.js — EElab77
 * ─────────────────────────────────────────────────────────────
 * The Arduino multimeter firmware stored as a JS string in
 * window.EELAB_MULTIMETER_SKETCH.
 *
 * Used by multimeter-controls.js to populate the Sketch modal
 * and power the Copy / Download buttons.
 *
 * Load this before multimeter-controls.js.
 *
 * ── HARDWARE WIRING ─────────────────────────────────────────
 *
 * DC Voltage (0–5 V):
 *   Signal → A0    GND → GND
 *
 * DC Voltage (0–50 V):
 *   Signal → [R1=47kΩ] → A0 → [R2=5.1kΩ] → GND
 *
 * DC Current (0–1 A):
 *   Signal(+) → [R_shunt=1Ω 1W] → A0
 *   Other end of shunt → GND
 *
 * Resistance / Continuity / Diode:
 *   Arduino 5V → [R_known=10kΩ] → A0 → [R_unknown] → GND
 *
 * Frequency / Duty Cycle:
 *   Signal → D8    GND → GND
 *   (0–5 V logic only — use a voltage divider for 3.3 V signals)
 *
 * ── PROTOCOL ────────────────────────────────────────────────
 *
 * Every measurement is sent as a 6-byte frame:
 *   [0xAB] [mode] [val_hi] [val_lo] [flags] [0xCD]
 *
 * mode:
 *   0x01 = DC Voltage   (val = millivolts,  e.g. 2500 = 2.500 V)
 *   0x02 = DC Current   (val = microamps,   e.g. 1500 = 1.500 mA)
 *   0x03 = Resistance   (val = ohms,        e.g. 10000 = 10.000 kΩ)
 *   0x04 = Continuity   (val = ohms,        flags bit1 = closed)
 *   0x05 = Frequency    (val = decihertz,   e.g. 10000 = 1000.0 Hz)
 *   0x06 = Duty Cycle   (val = tenths of %, e.g. 500 = 50.0 %)
 *   0x07 = Diode        (val = millivolts forward voltage)
 *
 * flags:
 *   bit 0 = overrange (OL)
 *   bit 1 = continuity closed (resistance < 50 Ω)
 *   bit 2 = diode forward biased (Vf < 1.2 V)
 *
 * Commands from web app (single ASCII byte):
 *   '1' = set mode DC Voltage
 *   '2' = set mode DC Current
 *   '3' = set mode Resistance
 *   '4' = set mode Continuity
 *   '5' = set mode Frequency
 *   '6' = set mode Duty Cycle
 *   '7' = set mode Diode
 *   'H' = hold (pause sending, keep last reading)
 *   'R' = resume sending
 *   'I' = info / handshake
 *   'T' = self-test
 * ─────────────────────────────────────────────────────────────
 */

window.EELAB_MULTIMETER_SKETCH = [
  '// ============================================================',
  '//  EElab77 — Multimeter Firmware v1.0',
  '//  Compatible: Arduino Uno, Nano, Mega (AVR, 5V, 16MHz)',
  '//',
  '//  WIRING SUMMARY:',
  '//  DC Voltage (0-5V)  : Signal → A0, GND → GND',
  '//  DC Voltage (0-50V) : Signal → [47kΩ] → A0 → [5.1kΩ] → GND',
  '//  DC Current         : Signal(+) → [1Ω shunt] → A0 → GND',
  '//  Resistance/Cont    : 5V → [10kΩ] → A0 → [R_unknown] → GND',
  '//  Frequency/Duty     : Signal → D8, GND → GND (0-5V only)',
  '//',
  '//  See EElab77 Multimeter page for full wiring diagrams.',
  '// ============================================================',
  '',
  '// ── Pin definitions ──────────────────────────────────────',
  '#define ANALOG_PIN   A0   // All voltage/current/resistance modes',
  '#define FREQ_PIN     8    // Frequency and duty cycle input (ICP1)',
  '#define BAUD_RATE    115200',
  '',
  '// ── Protocol constants ───────────────────────────────────',
  '#define FRAME_START  0xAB',
  '#define FRAME_END    0xCD',
  '',
  '// ── Mode codes ───────────────────────────────────────────',
  '#define MODE_DCV   1',
  '#define MODE_DCA   2',
  '#define MODE_RES   3',
  '#define MODE_CONT  4',
  '#define MODE_FREQ  5',
  '#define MODE_DUTY  6',
  '#define MODE_DIODE 7',
  '',
  '// ── Flag bits ────────────────────────────────────────────',
  '#define FLAG_OVERRANGE  0x01',
  '#define FLAG_CONT_CLOSE 0x02',
  '#define FLAG_DIODE_FWD  0x04',
  '',
  '// ── Calibration constants ────────────────────────────────',
  '// Voltage divider for 50V range: R1=47kΩ, R2=5.1kΩ',
  '// Scale factor = (R1+R2)/R2 = 52100/5100 ≈ 10.216',
  '#define VDIV_50V_SCALE   10216UL  // x1000 fixed-point',
  '',
  '// Known resistor for resistance measurement (ohms)',
  '#define R_KNOWN_OHMS     10000UL',
  '',
  '// Shunt resistor for current measurement (milliohms)',
  '#define R_SHUNT_MOHMS    1000UL   // 1.000 Ω',
  '',
  '// Continuity threshold (ohms)',
  '#define CONT_THRESHOLD   50',
  '',
  '// Overrange threshold for resistance (> 1MΩ is OL)',
  '#define RES_OVERRANGE    1000000UL',
  '',
  '// ADC reference voltage in microvolts (5.000V)',
  '#define VREF_UV          5000000UL',
  '',
  '// ── State ────────────────────────────────────────────────',
  'uint8_t  currentMode = MODE_DCV;',
  'bool     holdMode    = false;',
  'bool     streaming   = true;',
  '',
  '// ── Frequency measurement state ──────────────────────────',
  '// Uses Timer1 Input Capture for accurate frequency measurement.',
  '// ICR1 captures the timer value on each rising/falling edge.',
  'volatile uint32_t freqPeriodSum   = 0;',
  'volatile uint16_t freqSampleCount = 0;',
  'volatile uint32_t highTimeSum     = 0;',
  'volatile uint16_t lastCapture     = 0;',
  'volatile bool     inHighPhase     = false;',
  'volatile uint16_t highStart       = 0;',
  '',
  'unsigned long lastSendTime = 0;',
  'const uint16_t SEND_INTERVAL_MS = 100; // 10 readings/second',
  '',
  '// ── Frame sending ─────────────────────────────────────────',
  'void sendFrame(uint8_t mode, uint16_t value, uint8_t flags) {',
  '  Serial.write(FRAME_START);',
  '  Serial.write(mode);',
  '  Serial.write((value >> 8) & 0xFF);',
  '  Serial.write(value & 0xFF);',
  '  Serial.write(flags);',
  '  Serial.write(FRAME_END);',
  '}',
  '',
  '// ── ADC helpers ──────────────────────────────────────────',
  '// Read ADC N times and return average (reduces noise)',
  'uint16_t adcAverage(uint8_t pin, uint8_t samples) {',
  '  uint32_t sum = 0;',
  '  for (uint8_t i = 0; i < samples; i++) {',
  '    sum += analogRead(pin);',
  '    delayMicroseconds(200);',
  '  }',
  '  return (uint16_t)(sum / samples);',
  '}',
  '',
  '// Convert ADC count to microvolts',
  'uint32_t adcToMicrovolts(uint16_t adcVal) {',
  '  return ((uint32_t)adcVal * VREF_UV) / 1023UL;',
  '}',
  '',
  '// ── DC Voltage measurement ────────────────────────────────',
  'void measureDCV() {',
  '  uint16_t raw = adcAverage(ANALOG_PIN, 16);',
  '  uint32_t uv  = adcToMicrovolts(raw);',
  '',
  '  // Apply voltage divider scaling for 50V range',
  '  // Comment out the next line if using 5V range only',
  '  uv = (uv * VDIV_50V_SCALE) / 1000UL;',
  '',
  '  // Convert to millivolts for frame (max 65535 mV = 65.5 V)',
  '  uint16_t mv    = (uint16_t)(uv / 1000UL);',
  '  uint8_t  flags = (mv > 60000) ? FLAG_OVERRANGE : 0;',
  '  sendFrame(MODE_DCV, mv, flags);',
  '}',
  '',
  '// ── DC Current measurement ────────────────────────────────',
  'void measureDCA() {',
  '  uint16_t raw = adcAverage(ANALOG_PIN, 16);',
  '  uint32_t uv  = adcToMicrovolts(raw);',
  '',
  '  // I = V / R_shunt.  uv in µV, R_shunt in mΩ → result in µA',
  '  // I(µA) = V(µV) / R(mΩ) × 1000',
  '  uint32_t ua    = (uv * 1000UL) / R_SHUNT_MOHMS;',
  '  uint8_t  flags = (ua > 2000000UL) ? FLAG_OVERRANGE : 0;',
  '',
  '  // Send as microamps (val in frame = µA / 10 to fit 16-bit)',
  '  // Max = 65535 × 10 = 655350 µA ≈ 655 mA',
  '  uint16_t val = (uint16_t)min(ua / 10UL, 65535UL);',
  '  sendFrame(MODE_DCA, val, flags);',
  '}',
  '',
  '// ── Resistance measurement ────────────────────────────────',
  '// Circuit: 5V → [R_KNOWN] → A0 → [R_UNKNOWN] → GND',
  '// Va = 5V × R_unknown / (R_known + R_unknown)',
  '// R_unknown = R_known × Va / (5V - Va)',
  'void measureRES() {',
  '  uint16_t raw = adcAverage(ANALOG_PIN, 16);',
  '',
  '  uint8_t flags = 0;',
  '  uint16_t val  = 0;',
  '',
  '  if (raw <= 5) {',
  '    // Input pulled to GND — open circuit',
  '    flags = FLAG_OVERRANGE;',
  '    val   = 0;',
  '  } else if (raw >= 1018) {',
  '    // Input pulled to VCC — short or zero resistance',
  '    val   = 0;',
  '    flags = 0;',
  '  } else {',
  '    // R_unknown = R_known × raw / (1023 - raw)',
  '    uint32_t r = (R_KNOWN_OHMS * (uint32_t)raw) / (1023UL - raw);',
  '    if (r > RES_OVERRANGE) {',
  '      flags = FLAG_OVERRANGE;',
  '      val   = 65535;',
  '    } else {',
  '      val = (uint16_t)min(r, 65535UL);',
  '    }',
  '  }',
  '  sendFrame(MODE_RES, val, flags);',
  '}',
  '',
  '// ── Continuity measurement ───────────────────────────────',
  'void measureCONT() {',
  '  uint16_t raw = adcAverage(ANALOG_PIN, 8);',
  '  uint8_t  flags = 0;',
  '  uint16_t val   = 0;',
  '',
  '  if (raw >= 1018) {',
  '    val   = 0;',
  '    flags = FLAG_CONT_CLOSE; // Short / zero resistance',
  '  } else if (raw <= 5) {',
  '    flags = FLAG_OVERRANGE;',
  '    val   = 65535;',
  '  } else {',
  '    uint32_t r = (R_KNOWN_OHMS * (uint32_t)raw) / (1023UL - raw);',
  '    val = (uint16_t)min(r, 65535UL);',
  '    if (r < CONT_THRESHOLD) flags |= FLAG_CONT_CLOSE;',
  '  }',
  '  sendFrame(MODE_CONT, val, flags);',
  '}',
  '',
  '// ── Diode test ───────────────────────────────────────────',
  '// Uses resistance circuit but reports forward voltage directly.',
  '// Vf = Va (voltage at A0) when diode is forward biased.',
  'void measureDIODE() {',
  '  uint16_t raw = adcAverage(ANALOG_PIN, 16);',
  '  uint32_t uv  = adcToMicrovolts(raw);',
  '  uint16_t mv  = (uint16_t)(uv / 1000UL);',
  '',
  '  uint8_t flags = 0;',
  '  // Forward biased silicon diode: Vf typically 0.4V–0.8V',
  '  // Forward biased Schottky:      Vf typically 0.15V–0.4V',
  '  // If Vf > 1.2V or ADC reads 0, diode is reverse biased or open',
  '  if (mv > 50 && mv < 1200) {',
  '    flags = FLAG_DIODE_FWD;',
  '  } else if (raw <= 5) {',
  '    flags = FLAG_OVERRANGE; // Open',
  '  }',
  '  sendFrame(MODE_DIODE, mv, flags);',
  '}',
  '',
  '// ── Frequency / duty cycle ────────────────────────────────',
  '// Timer1 Input Capture on ICP1 (pin D8).',
  '// Measures period and high-time for both freq and duty.',
  '',
  'void setupFreqTimer() {',
  '  // Timer1: normal mode, prescaler 8 (2MHz tick = 0.5µs)',
  '  // ICP1 on rising edge first',
  '  TCCR1A = 0;',
  '  TCCR1B = (1<<ICES1) | (1<<CS11); // rising edge, prescaler 8',
  '  TIFR1  = (1<<ICF1);              // clear any pending flag',
  '  TIMSK1 = (1<<ICIE1);             // enable input capture interrupt',
  '  freqPeriodSum   = 0;',
  '  freqSampleCount = 0;',
  '  highTimeSum     = 0;',
  '  inHighPhase     = false;',
  '}',
  '',
  'void teardownFreqTimer() {',
  '  TIMSK1 = 0; // disable interrupt',
  '  TCCR1B = 0; // stop timer',
  '}',
  '',
  'ISR(TIMER1_CAPT_vect) {',
  '  uint16_t captured = ICR1;',
  '  uint16_t period   = captured - lastCapture;',
  '  lastCapture       = captured;',
  '',
  '  if (TCCR1B & (1<<ICES1)) {',
  '    // Was rising edge — start of high phase',
  '    // Add the period from the previous falling edge',
  '    if (freqSampleCount > 0) {',
  '      freqPeriodSum += period;',
  '    }',
  '    freqSampleCount++;',
  '    highStart   = captured;',
  '    inHighPhase = true;',
  '    // Switch to falling edge capture',
  '    TCCR1B &= ~(1<<ICES1);',
  '  } else {',
  '    // Was falling edge — end of high phase',
  '    if (inHighPhase) {',
  '      highTimeSum += (captured - highStart);',
  '      inHighPhase  = false;',
  '    }',
  '    // Switch back to rising edge',
  '    TCCR1B |= (1<<ICES1);',
  '  }',
  '}',
  '',
  'void measureFREQ() {',
  '  // Collect data for SEND_INTERVAL_MS then compute',
  '  uint32_t pSum;',
  '  uint32_t hSum;',
  '  uint16_t count;',
  '',
  '  // Atomic read of volatile variables',
  '  cli();',
  '  pSum  = freqPeriodSum;',
  '  hSum  = highTimeSum;',
  '  count = freqSampleCount;',
  '  freqPeriodSum   = 0;',
  '  freqSampleCount = 0;',
  '  highTimeSum     = 0;',
  '  sei();',
  '',
  '  if (count < 2) {',
  '    // Not enough edges — signal too slow or absent',
  '    sendFrame(MODE_FREQ, 0, FLAG_OVERRANGE);',
  '    return;',
  '  }',
  '',
  '  // Average period in timer ticks (0.5µs each at prescaler 8)',
  '  // Period (µs) = pSum / count × 0.5',
  '  // Freq (dHz)  = 10000000 / period_us / 10 = 1000000 / period_us',
  '  uint32_t avgPeriodTicks = pSum / count;',
  '  // avgPeriodTicks × 0.5µs = period in µs',
  '  // freq_Hz = 1e6 / period_µs = 2e6 / avgPeriodTicks',
  '  // freq_dHz = freq_Hz × 10 = 20e6 / avgPeriodTicks',
  '  uint32_t freqDHz = 20000000UL / avgPeriodTicks;',
  '',
  '  // Duty cycle in tenths of percent',
  '  uint32_t dutyTenths = 0;',
  '  if (pSum > 0) {',
  '    dutyTenths = (hSum * 1000UL) / pSum;',
  '  }',
  '',
  '  uint8_t  flags   = (freqDHz > 80000000UL) ? FLAG_OVERRANGE : 0;',
  '  uint16_t freqVal = (uint16_t)min(freqDHz, 65535UL);',
  '',
  '  if (currentMode == MODE_FREQ) {',
  '    sendFrame(MODE_FREQ, freqVal, flags);',
  '  } else {',
  '    sendFrame(MODE_DUTY, (uint16_t)min(dutyTenths,1000UL), flags);',
  '  }',
  '}',
  '',
  '// ── Command handler ──────────────────────────────────────',
  'void handleCommand(char cmd) {',
  '  switch(cmd) {',
  '    case \'1\': currentMode = MODE_DCV;',
  '              teardownFreqTimer(); break;',
  '    case \'2\': currentMode = MODE_DCA;',
  '              teardownFreqTimer(); break;',
  '    case \'3\': currentMode = MODE_RES;',
  '              teardownFreqTimer(); break;',
  '    case \'4\': currentMode = MODE_CONT;',
  '              teardownFreqTimer(); break;',
  '    case \'5\': currentMode = MODE_FREQ;',
  '              setupFreqTimer(); break;',
  '    case \'6\': currentMode = MODE_DUTY;',
  '              setupFreqTimer(); break;',
  '    case \'7\': currentMode = MODE_DIODE;',
  '              teardownFreqTimer(); break;',
  '    case \'H\': holdMode   = true;   break;',
  '    case \'R\': holdMode   = false;  break;',
  '    case \'I\':',
  '      Serial.println("{\\"device\\":\\"EElab77-DMM\\",\\"version\\":\\"1.0\\",\\"baud\\":115200}");',
  '      break;',
  '    case \'T\':',
  '      holdMode = true;',
  '      Serial.println("=== EElab77 DMM Self-Test ===");',
  '      Serial.print("Mode: "); Serial.println(currentMode);',
  '      Serial.print("A0 raw: "); Serial.println(analogRead(ANALOG_PIN));',
  '      Serial.print("D8 state: "); Serial.println(digitalRead(FREQ_PIN));',
  '      Serial.println("=== OK. Send R to resume. ===");',
  '      holdMode = false;',
  '      break;',
  '  }',
  '}',
  '',
  '// ── Setup ────────────────────────────────────────────────',
  'void setup() {',
  '  Serial.begin(BAUD_RATE);',
  '  pinMode(FREQ_PIN, INPUT);',
  '',
  '  // ADC: prescaler 32 for ~38kSPS (more averaging possible)',
  '  ADCSRA &= ~((1<<ADPS2)|(1<<ADPS1)|(1<<ADPS0));',
  '  ADCSRA |=  (1<<ADPS2)|(1<<ADPS0);',
  '  analogReference(DEFAULT);',
  '',
  '  // Warm up ADC',
  '  analogRead(ANALOG_PIN);',
  '  analogRead(ANALOG_PIN);',
  '}',
  '',
  '// ── Main loop ────────────────────────────────────────────',
  'void loop() {',
  '  // Handle incoming commands',
  '  while (Serial.available()) {',
  '    handleCommand((char)Serial.read());',
  '  }',
  '',
  '  if (holdMode) return;',
  '',
  '  unsigned long now = millis();',
  '  if (now - lastSendTime < SEND_INTERVAL_MS) return;',
  '  lastSendTime = now;',
  '',
  '  switch (currentMode) {',
  '    case MODE_DCV:   measureDCV();  break;',
  '    case MODE_DCA:   measureDCA();  break;',
  '    case MODE_RES:   measureRES();  break;',
  '    case MODE_CONT:  measureCONT(); break;',
  '    case MODE_FREQ:  measureFREQ(); break;',
  '    case MODE_DUTY:  measureFREQ(); break;',
  '    case MODE_DIODE: measureDIODE();break;',
  '  }',
  '}'
].join('\n');
