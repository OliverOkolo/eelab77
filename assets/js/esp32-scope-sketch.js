/**
 * esp32-scope-sketch.js — EElab77
 * ─────────────────────────────────────────────────────────────
 * The ESP32 oscilloscope firmware stored as a JS string in
 * window.EELAB_ESP32_SKETCH.
 *
 * Supports ESP32-S3 (native USB) and ESP32 Classic (USB bridge).
 * Load this before esp32-scope-controls.js.
 * ─────────────────────────────────────────────────────────────
 */

window.EELAB_ESP32_SKETCH = [

  '// ============================================================',
  '//  EElab77 — ESP32 Oscilloscope Firmware v1.0',
  '//',
  '//  Supports:',
  '//    - ESP32-S3      (native USB CDC + WiFi)',
  '//    - ESP32 Classic (USB bridge + WiFi)',
  '//',
  '//  BEFORE UPLOADING:',
  '//  1. Uncomment ONE board definition below',
  '//  2. Set WiFi credentials if using Station mode',
  '//  3. Install required libraries (Arduino Library Manager):',
  '//       - WebSockets by Markus Sattler (v2.3.6+)',
  '//       - ESP Async WebServer by lacamera (v1.2.3+)',
  '//       - AsyncTCP by dvarrel',
  '//  4. Board package: esp32 by Espressif Systems v2.0.11+',
  '//',
  '//  BOARD SELECTION IN ARDUINO IDE:',
  '//    ESP32-S3:      "ESP32S3 Dev Module"',
  '//                   Tools > USB Mode > USB-OTG (TinyUSB)',
  '//    ESP32 Classic: "ESP32 Dev Module"',
  '//',
  '//  WIRING:',
  '//    CH1: GPIO34  (input-only pin, 0-3.3V max)',
  '//    CH2: GPIO35  (input-only pin, 0-3.3V max)',
  '//    GND: GND',
  '//    WARNING: NEVER exceed 3.3V on any ESP32 GPIO.',
  '//             Use a resistor divider for 5V signals.',
  '// ============================================================',
  '',

  '// ── Board selection — uncomment ONE line ────────────────',
  '#define BOARD_ESP32_S3',
  '// #define BOARD_ESP32_CLASSIC',
  '',

  '#if !defined(BOARD_ESP32_S3) && !defined(BOARD_ESP32_CLASSIC)',
  '  #error "Select a board: BOARD_ESP32_S3 or BOARD_ESP32_CLASSIC"',
  '#endif',
  '#if defined(BOARD_ESP32_S3) && defined(BOARD_ESP32_CLASSIC)',
  '  #error "Only one board can be selected at a time."',
  '#endif',
  '',

  '// ── Board-specific USB includes ──────────────────────────',
  '#if defined(BOARD_ESP32_S3)',
  '  #include "USB.h"',
  '  #include "USBCDC.h"',
  '  USBCDC SerialUSB;',
  '  #define SERIAL_OUT SerialUSB',
  '#else',
  '  // ESP32 Classic uses hardware UART via CH340/CP2102',
  '  #define SERIAL_OUT Serial',
  '#endif',
  '',

  '// ── Common includes ──────────────────────────────────────',
  '#include <WiFi.h>',
  '#include <WebSocketsServer.h>',
  '#include <WebServer.h>',
  '#include <Preferences.h>',
  '',

  '// ── WiFi configuration ───────────────────────────────────',
  '// AP mode — ESP32 creates its own hotspot (default)',
  '#define WIFI_AP_SSID      "EElab77-Scope"',
  '#define WIFI_AP_PASSWORD  "eelab77pw"',
  '',
  '// Station mode — join existing WiFi (leave blank to use AP)',
  '#define WIFI_STA_SSID     ""',
  '#define WIFI_STA_PASSWORD ""',
  '',

  '// ── Pin definitions ──────────────────────────────────────',
  '#define CH1_PIN   34   // ADC1_CH6 — input only, 0-3.3V',
  '#define CH2_PIN   35   // ADC1_CH7 — input only, 0-3.3V',
  '#define LED_PIN    2   // Built-in LED',
  '',

  '// ── Protocol ─────────────────────────────────────────────',
  '#define FRAME_START  0xAB',
  '#define FRAME_END    0xCD',
  '#define BAUD_RATE    500000  // ESP32 Classic wired mode only',
  '',

  '// ── ADC nonlinearity correction (16-point piecewise) ────',
  '// Corrects the S-curve characteristic of the ESP32 ADC.',
  'const uint16_t ADC_CORRECTION[16] = {',
  '     0,  248,  500,  753, 1005, 1256, 1504, 1750,',
  '  1994, 2237, 2480, 2724, 2968, 3210, 3596, 4095',
  '};',
  '',
  'uint16_t correctADC(uint16_t raw) {',
  '  if (raw >= 4095) return 4095;',
  '  uint8_t  seg  = raw >> 8;',
  '  uint16_t frac = raw & 0xFF;',
  '  if (seg >= 15) return ADC_CORRECTION[15];',
  '  uint16_t lo = ADC_CORRECTION[seg];',
  '  uint16_t hi = ADC_CORRECTION[seg + 1];',
  '  return lo + (uint16_t)(((uint32_t)(hi - lo) * frac) >> 8);',
  '}',
  '',

  '// ── State ────────────────────────────────────────────────',
  'bool    streaming   = false;',
  'bool    ch2Enable   = true;',
  'bool    wifiActive  = false;',
  'bool    staMode     = false;',
  'uint8_t wsClientId  = 0;',
  'bool    wsConnected = false;',
  '',

  '// ── WebSocket and HTTP servers ────────────────────────────',
  'WebSocketsServer wsServer(81);',
  'WebServer        httpServer(80);',
  '',

  '// ── Frame sending ─────────────────────────────────────────',
  'uint8_t frameBuf[4];',
  '',
  'void sendSample(uint16_t val, uint8_t ch) {',
  '  frameBuf[0] = FRAME_START;',
  '  frameBuf[1] = val & 0xFF;',
  '  frameBuf[2] = ((val >> 8) & 0x0F) | (ch << 4);',
  '  frameBuf[3] = FRAME_END;',
  '  if (wsConnected) {',
  '    wsServer.sendBIN(wsClientId, frameBuf, 4);',
  '  } else {',
  '    SERIAL_OUT.write(frameBuf, 4);',
  '  }',
  '}',
  '',

  '// ── ADC setup ────────────────────────────────────────────',
  'void setupADC() {',
  '  analogSetAttenuation(ADC_11db);',
  '  analogSetWidth(12);',
  '  for (int i = 0; i < 8; i++) {',
  '    analogRead(CH1_PIN);',
  '    analogRead(CH2_PIN);',
  '  }',
  '}',
  '',

  '// ── WiFi setup ───────────────────────────────────────────',
  'void setupWiFi() {',
  '  String sta = String(WIFI_STA_SSID);',
  '  if (sta.length() > 0) {',
  '    staMode = true;',
  '    WiFi.mode(WIFI_STA);',
  '    WiFi.begin(WIFI_STA_SSID, WIFI_STA_PASSWORD);',
  '    SERIAL_OUT.print("Connecting to WiFi");',
  '    uint8_t attempts = 0;',
  '    while (WiFi.status() != WL_CONNECTED && attempts < 20) {',
  '      delay(500);',
  '      SERIAL_OUT.print(".");',
  '      attempts++;',
  '    }',
  '    if (WiFi.status() == WL_CONNECTED) {',
  '      SERIAL_OUT.print("\\nConnected. IP: ");',
  '      SERIAL_OUT.println(WiFi.localIP());',
  '      wifiActive = true;',
  '      return;',
  '    }',
  '    SERIAL_OUT.println("\\nSTA failed — falling back to AP.");',
  '  }',
  '  // AP mode',
  '  staMode = false;',
  '  WiFi.mode(WIFI_AP);',
  '  WiFi.softAP(WIFI_AP_SSID, WIFI_AP_PASSWORD);',
  '  WiFi.softAPConfig(',
  '    IPAddress(192,168,4,1),',
  '    IPAddress(192,168,4,1),',
  '    IPAddress(255,255,255,0)',
  '  );',
  '  SERIAL_OUT.print("AP started: ");',
  '  SERIAL_OUT.println(WIFI_AP_SSID);',
  '  SERIAL_OUT.print("IP: ");',
  '  SERIAL_OUT.println(WiFi.softAPIP());',
  '  wifiActive = true;',
  '}',
  '',

  '// ── WebSocket event handler ───────────────────────────────',
  'void onWSEvent(uint8_t id, WStype_t type,',
  '               uint8_t* payload, size_t length) {',
  '  switch (type) {',
  '    case WStype_CONNECTED:',
  '      wsClientId  = id;',
  '      wsConnected = true;',
  '      wsServer.sendTXT(id,',
  '        "{\\\"device\\\":\\\"EElab77-ESP32\\\","',
  '        "\\\"version\\\":\\\"1.0\\\","',
  '        "\\\"adc_bits\\\":12,\\\"vref\\\":3.3,"',
  '        "\\\"channels\\\":2}"',
  '      );',
  '      break;',
  '    case WStype_DISCONNECTED:',
  '      if (id == wsClientId) {',
  '        wsConnected = false;',
  '        streaming   = false;',
  '      }',
  '      break;',
  '    case WStype_TEXT:',
  '      if (length > 0) handleCommand((char)payload[0]);',
  '      break;',
  '    default:',
  '      break;',
  '  }',
  '}',
  '',

  '// ── HTTP server ───────────────────────────────────────────',
  'void setupHTTP() {',
  '  httpServer.on("/", HTTP_GET, []() {',
  '    String json = "{\\\"device\\\":\\\"EElab77-ESP32\\\","',
  '                  "\\\"ws_port\\\":81,"',
  '                  "\\\"streaming\\\":";',
  '    json += streaming ? "true" : "false";',
  '    json += ",\\\"mode\\\":\\\"";',
  '    json += staMode ? "sta" : "ap";',
  '    json += "\\\"}";',
  '    httpServer.send(200, "application/json", json);',
  '  });',
  '  httpServer.begin();',
  '}',
  '',

  '// ── Command handler ──────────────────────────────────────',
  'void handleCommand(char cmd) {',
  '  switch (cmd) {',
  '    case \'S\': streaming  = true;  break;',
  '    case \'P\': streaming  = false; break;',
  '    case \'2\': ch2Enable  = true;  break;',
  '    case \'1\': ch2Enable  = false; break;',
  '    case \'I\': {',
  '      String info = "{\\\"device\\\":\\\"EElab77-ESP32\\\","',
  '                    "\\\"version\\\":\\\"1.0\\\","',
  '                    "\\\"adc_bits\\\":12,\\\"vref\\\":3.3,"',
  '                    "\\\"channels\\\":2,\\\"baud\\\":";',
  '      info += String(BAUD_RATE);',
  '      info += ",\\\"wifi\\\":";',
  '      info += wifiActive ? "true" : "false";',
  '      info += "}";',
  '      if (wsConnected) wsServer.sendTXT(wsClientId, info);',
  '      else             SERIAL_OUT.println(info);',
  '      break;',
  '    }',
  '    case \'T\': {',
  '      streaming = false;',
  '      String r = "=== EElab77 ESP32 Self-Test ===\\n";',
  '      #if defined(BOARD_ESP32_S3)',
  '      r += "Board    : ESP32-S3\\n";',
  '      #else',
  '      r += "Board    : ESP32 Classic\\n";',
  '      #endif',
  '      r += "ADC bits : 12\\n";',
  '      r += "CH1 raw  : "; r += String(analogRead(CH1_PIN)); r += "\\n";',
  '      r += "CH2 raw  : "; r += String(analogRead(CH2_PIN)); r += "\\n";',
  '      r += "WiFi     : "; r += wifiActive ? "Active" : "Off"; r += "\\n";',
  '      r += "Mode     : "; r += staMode ? "Station" : "AP"; r += "\\n";',
  '      if (wifiActive) {',
  '        r += "IP       : ";',
  '        r += staMode',
  '          ? WiFi.localIP().toString()',
  '          : WiFi.softAPIP().toString();',
  '        r += "\\n";',
  '      }',
  '      r += "=== OK. Send S to start streaming. ===";',
  '      if (wsConnected) wsServer.sendTXT(wsClientId, r);',
  '      else             SERIAL_OUT.println(r);',
  '      break;',
  '    }',
  '    case \'A\':',
  '      // Switch to AP mode',
  '      streaming = false;',
  '      WiFi.disconnect();',
  '      delay(100);',
  '      WiFi.mode(WIFI_AP);',
  '      WiFi.softAP(WIFI_AP_SSID, WIFI_AP_PASSWORD);',
  '      staMode    = false;',
  '      wifiActive = true;',
  '      break;',
  '    default:',
  '      break;',
  '  }',
  '}',
  '',

  '// ── Serial command polling ────────────────────────────────',
  'void pollSerial() {',
  '  while (SERIAL_OUT.available()) {',
  '    handleCommand((char)SERIAL_OUT.read());',
  '  }',
  '}',
  '',

  '// ── LED heartbeat ─────────────────────────────────────────',
  'unsigned long lastLed = 0;',
  'bool          ledOn   = false;',
  '',
  'void updateLED() {',
  '  uint16_t interval = streaming ? 100 : 1000;',
  '  if (millis() - lastLed >= interval) {',
  '    lastLed = millis();',
  '    ledOn   = !ledOn;',
  '    digitalWrite(LED_PIN, ledOn);',
  '  }',
  '}',
  '',

  '// ── Setup ────────────────────────────────────────────────',
  'void setup() {',
  '  #if defined(BOARD_ESP32_S3)',
  '    USB.begin();',
  '    SerialUSB.begin(0);',
  '    delay(500);',
  '  #else',
  '    Serial.begin(BAUD_RATE);',
  '  #endif',
  '',
  '  pinMode(LED_PIN, OUTPUT);',
  '  setupADC();',
  '  setupWiFi();',
  '',
  '  if (wifiActive) {',
  '    wsServer.begin();',
  '    wsServer.onEvent(onWSEvent);',
  '    setupHTTP();',
  '  }',
  '',
  '  SERIAL_OUT.println("EElab77 ESP32 Scope ready.");',
  '}',
  '',

  '// ── Main loop ────────────────────────────────────────────',
  'void loop() {',
  '  if (wifiActive) {',
  '    wsServer.loop();',
  '    httpServer.handleClient();',
  '  }',
  '  pollSerial();',
  '  updateLED();',
  '  if (!streaming) return;',
  '  sendSample(correctADC((uint16_t)analogRead(CH1_PIN)), 0);',
  '  if (ch2Enable) {',
  '    sendSample(correctADC((uint16_t)analogRead(CH2_PIN)), 1);',
  '  }',
  '}'

].join('\n');
